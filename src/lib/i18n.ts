// Cookie functions
export const getLanguageFromCookie = (): Language => {
  if (typeof document === "undefined") return "en";
  
  const cookies = document.cookie.split(";");
  const cookie = cookies.find((c) => c.trim().startsWith(LANGUAGE_COOKIE_KEY + "="));
  
  if (cookie) {
    const value = cookie.split("=")[1];
    if (Object.keys(LANGUAGE_OPTIONS).includes(value)) {
      return value as Language;
    }
  }
  
  return "en";
};

export const setLanguageCookie = (language: Language): void => {
  if (typeof document === "undefined") return;
  
  const date = new Date();
  date.setTime(date.getTime() + COOKIE_EXPIRY_DAYS * 24 * 60 * 60 * 1000);
  const expires = `expires=${date.toUTCString()}`;
  
  document.cookie = `${LANGUAGE_COOKIE_KEY}=${language};${expires};path=/`;
};

